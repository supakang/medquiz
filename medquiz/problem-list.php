<div id="page-one">
	<p class="title"> Introduction </p>
	<ul> 
		<li>Developing a succinct, yet a thorough problem list with appropriate plan is an important skillset students develop during their clerkship.
		</li>
		<li>Creating a problem list allows clerks to thoroughly assess each issue and minimize the risk of missing an important issue. </li>
		<li>This interactive module is intended for pre-clerkship undergraduate medical students to be introduced to the basics on developing a problem list during their consultations.</li>
		<li>This module will not teach students on how to take a history, perform physical examination or interpret investigation results. </li>
		<li>  This module will assumes students can formulate differential diagnoses.</li>
	</ul>
</div>
<div id="page-two">
	<p class="title"> What is a Problem List</p>
	<ul>
		<li>A clinician develops a “Problem List” based on their history, physical examination and investigations</li>
		<li>A problem list addresses all of the important presenting symptoms, lab abnormalities, the working diagnosis</li>
		<li>Each problem on the list should includes differential diagnoses</li>
		<li>Each problem on the list should easily convey the plan (including further investigations and management).</li>
 		<li>A problem list should be prioritized with the most important problem first (eg. sepsis) and less important problems (eg. slightly low magnesium) later.</li>
	</ul>
</div>
<div id="page-three">
	<p class="title">Systematic Approach to Developing a Problem List</p>
	<ol>
		<li>What is the most pressing issue that the patient has?</li>
		<li>What is the most likely working diagnosis? What are the possible alternative diagnoses?</li>
		<li>Are there any investigations that should be done?</li>
		<li>What are the management plans that must be made?</li>
		<li>Are there any other issues that should be addressed in this visit?</li>
		<li>Does the patient need to be admitted or can this be managed as an outpatient?</li>
		<li>What is the disposition plan?</li>
	</ol>
</div>
<div id="page-four">
	<p class="title">1. What is the most pressing issue the patient is presenting with?</p>
	<ul>
		<li>What is the most important symptom the patient is presenting with? 
			<ul>
				<li>If a patient is presenting with increased fatigue and crushing retrosternal chest pain on exertion, the chest pain should be at the top of the problem list.
				</li>
			</ul>
		<li>A patient’s chief complaint is not necessarily the most pressing issue.
			<ul>
				<li>If a patient presents to the ED complaining of leg swelling and was found to also have increased SOB on exertion on history, increased SOB on exertion could be the first issue on your problem list.</li>
			</ul>
		</li>
		<li>Make each issues a symptom the patient is presenting with, not the working diagnosis.
			<ul>
				<li>If a patient is presenting with increased SOB with working diagnosis pneumonia, the issue should be dyspnea and not pneumonia.</li>
				<li>This allows clerks to avoid being fixated on a specific diagnosis and consider all possible alternative diagnosis of increased SOB.</li>
			</ul>
		</li>
	</ul>
</div>
<div id="page-five">
	<p class="title">2. What is the most likely working diagnosis?</p>
	<ul>
		<li>A good problem list should have a single most likely diagnosis supported by the history, physical examination and investigations.</li>
		<li>A reasonable differential diagnosis should be included to show that you have thought about other alternative diagnosis.
			<ul>
				<li>You do not have to list every possible DDx and in fact, it is better to write down few reasonable differentials and how you will rule in or out the DDx.</li>
			</ul>
		</li>
		<li>Consider life-threatening differential diagnosis that you cannot miss.
		<ul>
			<li>For example, if a patient presented with acute CHF exacerbation, ACS as a cause of trigger should be ruled out as this is a life-threatening etiology, despite the fact medication/salt-restriction non-compliance is a more likely cause.</li>
		</ul>
	</ul>
</div>
<div id="page-six">
	<p class="title">3. Are there any investigations that should be done? </p>
	<ul>
		<li>Make sure to review what investigations have already been done (or ordered) by the ED.</li>
		<li>Do you need any more investigations to support your diagnosis?
			<ul>
				<li>These can include investigations to:
					<ul>
						<li>increased the likelihood of your working diagnosis</li>
						<li>rule out other possible differentials</li>
						<li>rule out a life-threatening differential or investigations for prognosis.</li>
					</ul>
				</li>
				<li>Sometimes making the diagnosis does not require further investigations (maybe they’ve already been done). </li>
			</ul>
		</li>
		<li>Are there any alternative diagnosis that must be ruled-out with an investigation? 
			<ul>
				<li>
					If a patient has dyspnea and has COPD exacerbation as the most likely diagnosis, consider ordering a BNP could rule out heart failure as an alternative diagnosis.
				</li>
			</ul>
		</li>

		<li>How will the investigations change the course of management? 
			<ul>
				<li>Think about how results from particular investigations will change your management.</li> 
				<li>If a patient has high pre-test likelihood of pulmonary embolism, ordering D-dimer will not change your course of management as you would proceed with CTPE regardless of the results from the D-dimer.</li> 
			</ul>
		</li>
	</ul>
</div>
<div id="page-seven">
	<p class="title">4. What are the management plans that must be made?</p>
	<ul>
		<il>A good problem list should include a clear management plan for each issues.
			<ul>
				<li>These can be symptomatic control, therapeutic medical treatment, procedural managements, a referral to surgical specialty, etc.</li>
			</ul>
		</il>
		<li>A management plan should include both the short-term and long-term management goal. 
			<ul>
				<li>A short-term management plan for COPD exacerbation may include a SABA agonist/anticholinergic puffer, steroids and antibiotic therapy.</li>
				<li>A long-term management plan can include smoking cessation and pulmonary function test as an outpatient basis after discharge for prognostication.</li>
			</ul>
		</li>
		<li>If a management plan include any therapeutic management, they should include the exact dose, route and the duration. 
			<ul>
				<li>For antibiotic therapy in COPD exacerbation, a management plan could include moxifloxacin 400 mg PO once daily for 5 days with the stop date of ____. </li>
			</ul>
		</li>
	</ul>
</div>
<div id="page-eight">
	<p class="title">5. Are there any other issues that should be addressed in this visit?</p>
	<ul>
		<li>Include all symptoms, syndromes or lab abnormalities that are important.
			<ul>
				<li>It can be difficult to know which symptoms or lab abnormalities are important and which ones aren’t as there is no perfect answer and it depends on each clinical scenario.</li> 
				<li>If a patient has myriad of symptoms, focus on ones that needs to be absolutely addressed this visit. </li>
			</ul>
		</li>
		<li>Does the patient have any known medical issues that needs to be managed this visit?
			<ul>
				<li>For example, a patient admitted for biliary colic with known history T2DM on insulin will likely need insulin adjustments, and therefore T2DM should included as a separate item in the problem list.</li>
			</ul>
		</li>
		<li>What are other issues that should be anticipated for the patient?
			<ul>
				<li>If a patient is known to have a history of alcohol abuse, alcohol withdrawal should be an issue despite the fact the patient may not be showing any symptoms now, as preventative management can be made now. Thus, alcohol abuse should be a discrete item in the problem list.</li>
			</ul>
		</li>
	</ul>
</div>
<div id="page-nine">
	<p class="title">6. Does the patient need to be admitted or can this be managed as an outpatient?</p>
	<ul>
		<li>This is an important question as management of the patient changes depend on whether they are going to be admitted.</li>
		<li>There are specific considerations a problem list should include if a patient is being admitted.
			<ul>
				<li>For example, problem list should include information on best practices (IV fluids, foley, feeding tubes), DVT prophylaxis and code status of all admitted patients.</li>
			</ul>
		</li>
		<li>There are also key considerations a problem list should include if a patient is not being admitted.
		<ul> 
			<li>Do they require a follow-up? How will they be followed-up? How quickly will they need to be seen? Has the patient been referred already? If so make it clear on the problem list who the referral has been sent to.</li>
		</ul>
	</ul>
</div>
<div id="page-ten">
	<p class="title">7. What is the disposition plan? </p>
	<p>IE. Where is the patient going when their medical/surgical issues are resolved?</p>
	<ul>
		<li>Whenever a patient is being admitted, a problem list should include the plan on disposition.</li>
		<li>Disposition plans in an initial consultation note can be less detailed compared to a discharge summary or progress notes as course in hospital will influence the disposition plan.
			<ul>
				<li>It is helpful to have thought about the disposition plan as you are admitting the patient.</li> 
				<li>Likely disposition to home in 3 days vs will need transfer to assisted-living paints two completely different course in hospital for the readers.</li>
			</ul>
		</li>
		<li>A disposition plan should include:
			<ul>
				<li>Parameters for sending the patient home (i.e. afebrile, tolerating PO abx and appearing well).</li>
				<li>Where the patient will go (i.e. home, assisted-living, no fixed address).</li>
				<li>Who they will go home with (do they have family or are they living alone).</li>
				<li>Any follow-up plan? 
					<ul>
						<li>Do they have a GP to follow-up with?</li>
						<li>Will they likely need to see a specialist?</li>
					</ul>
				<li>Need to be seen by physiotherapist or an occupational therapist prior to discharge?</li> 

			</ul>
		</li>

